;========
; Font
;========

org 0x6000
use16

; Set cursor
mov cx,0x0407
mov ah,0x01
int 10h

; Set font for TM character
mov ax,0x1100
mov bh,0x0C
mov bl,0
mov cx,1
mov dx,0x93
xor bp,bp
mov es,bp
mov bp,font_93
int 10h

; Set the new font
;as described by bytes
mov ax,0x1100
mov bh,0x0C
mov bl,0
mov cx,3
mov dx,0x61
xor bp,bp
mov es,bp
mov bp,font_small
int 10h

; Set new prompt
mov dx,prompt
mov ah,0x0c
int 61h

;Return to OS
ret

prompt:
db 'Font1 by Hurd',0x93,'>',0
font_small:
times 2 db 0
db 0x1c,0x22,0x42,0x02,0x3e
times 3 db 0x42
db 0x22,0x1d

db 0x00,0xc0,0x40,0x40,0x70,0x48,0x44,0x44,0x44,0x48,0x70,0x80
db 0x00,0x00,0x00,0x38,0x44,0x82,0x80,0x80,0x82,0x42,0x24,0x18
font_93:
db 0x00,0x00,0x00
db 0xfb
db 0x55
db 0x51,0x51
times 5 db 0

times 512-($-$$) db 0x90